const passport = require("passport");

module.exports = (app) => {
  app.get(
    "/auth/google",
    passport.authenticate("google", {
      scope: ["profile", "email"],
    })
  );

  // Takes incoming request and authenticates the user. Passes on the request
  app.get(
    "/auth/google/callback",
    passport.authenticate("google"),
    (req, res) => {
      // need to verify if redirect is from Passport
      res.redirect("/surveys");
    }
  );

  app.get("/api/logout", (req, res) => {
    req.logout();
    res.redirect("/");
  });

  app.get("/api/current_user", (req, res) => {
    res.send(req.user);
  });
};
